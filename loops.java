public class loops {
    public static void main(String[] args) {
        //for döngüsü
        for(int i=1;i<10;i++){
            System.out.println(i);

        }
        //while döngüsü
        int i=1;
        while(i<10){
            System.out.println(i);
            i++;
        }
        //do-while döngüsü
        do{
            System.out.println(i);
            i++;
            
        }while(i<10);
        
    }

}
